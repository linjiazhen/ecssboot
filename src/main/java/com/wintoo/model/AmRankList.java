public class AmRankList {
    private String buildname;
    private String buildid;
    private int buildlevel;
    private double energy;
    private double aenergy;
    private double benergy;
    private double cenergy;
    private double denergy;
    private double zenergy;

    public String getBuildname() {
        return buildname;
    }

    public void setBuildname(String buildname) {
        this.buildname = buildname;
    }

    public String getBuildid() {
        return buildid;
    }

    public void setBuildid(String buildid) {
        this.buildid = buildid;
    }


    public int getBuildlevel() {
        return buildlevel;
    }

    public void setBuildlevel(int buildlevel) {
        this.buildlevel = buildlevel;
    }

    public double getEnergy() {
        return energy;
    }

    public void setEnergy(double energy) {
        this.energy = energy;
    }

    public double getAenergy() {
        return aenergy;
    }

    public void setAenergy(double aenergy) {
        this.aenergy = aenergy;
    }

    public double getBenergy() {
        return benergy;
    }

    public void setBenergy(double benergy) {
        this.benergy = benergy;
    }

    public double getCenergy() {
        return cenergy;
    }

    public void setCenergy(double cenergy) {
        this.cenergy = cenergy;
    }

    public double getDenergy() {
        return denergy;
    }

    public void setDenergy(double denergy) {
        this.denergy = denergy;
    }

    public double getZenergy() {
        return zenergy;
    }

    public void setZenergy(double zenergy) {
        this.zenergy = zenergy;
    }
}
