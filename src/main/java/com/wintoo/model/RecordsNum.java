public class RecordsNum {
    private int build;
    private int organ;
    private int gateway;
    private int ammeter;
    private int watermeter;
    private int warning;

    public int getBuild() {
        return build;
    }

    public void setBuild(int build) {
        this.build = build;
    }

    public int getOrgan() {
        return organ;
    }

    public void setOrgan(int organ) {
        this.organ = organ;
    }

    public int getGateway() {
        return gateway;
    }

    public void setGateway(int gateway) {
        this.gateway = gateway;
    }

    public int getAmmeter() {
        return ammeter;
    }

    public void setAmmeter(int ammeter) {
        this.ammeter = ammeter;
    }

    public int getWatermeter() {
        return watermeter;
    }

    public void setWatermeter(int watermeter) {
        this.watermeter = watermeter;
    }

    public int getWarning() {
        return warning;
    }

    public void setWarning(int warning) {
        this.warning = warning;
    }
}
